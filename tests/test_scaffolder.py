class _FakeSignals:
    """Minimal RepoSignals shape for helper tests."""

    primary_languages = ["react", "typescript"]
    file_count = 42
    manifests = {"package.json": "{}"}
    docs = {"README.md": "# x"}
    fingerprint_json = "{}"
    git_log = "abc init"
    git_authors = "1\tTest"


def test_offline_body_lists_facts_and_sections() -> None:
    body = _offline_body(_VISION_SECTIONS, _FakeSignals())  # type: ignore[arg-type]
    assert "techs: react, typescript" in body
    assert "files: 42" in body
    assert "manifests: package.json" in body
    assert "docs: README.md" in body
    for section in _VISION_SECTIONS:
        assert section in body
        assert "_TODO:_" in body


def test_build_prompt_includes_facts_and_sections() -> None:
    prompt = _build_prompt(
        title="DESIGN.md",
        sections=_DESIGN_SECTIONS,
        signals=_FakeSignals(),  # type: ignore[arg-type]
    )
    assert "Generate `DESIGN.md`" in prompt
    assert "### Tech fingerprint" in prompt
    assert "package.json" in prompt
    assert "README.md" in prompt
    for section in _DESIGN_SECTIONS:
        assert section.lstrip("# ").strip() in prompt


def test_add_header_prepends_banner_and_strips_duplicate_h1() -> None:
    body = "# Vision\n\nbody text\n"
    result = _add_header("vision", "2026-05-07", body)

    assert result.startswith("# Vision\n")
    assert "auto-generated by `sendsprint init` on 2026-05-07" in result
    assert result.count("# Vision\n") == 1
    assert "body text" in result


def test_section_constants_have_expected_shape() -> None:
    for sections in (_VISION_SECTIONS, _DOMAIN_SECTIONS, _DESIGN_SECTIONS, _PATTERNS_SECTIONS):
        assert len(sections) >= 5
        assert all(s.startswith("## ") for s in sections)
