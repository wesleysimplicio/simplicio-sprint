def test_generate_llm_calls_client_per_spec(tmp_path: Path) -> None:
    _seed_repo(tmp_path)
    fake = _FakeLlm()
    scaf = Scaffolder(tmp_path, llm=fake)
    signals = scaf.discover()

    outputs = scaf.generate(signals, offline=False)

    assert len(fake.calls) == 4
    systems = {system for _, system in fake.calls}
    assert systems == {Scaffolder.PROMPT_HEADER}
    for key, body in outputs.items():
        assert body.startswith(f"# {key.capitalize()}\n")
        assert "Generated body for" in body


