def test_generate_offline_builds_all_four_specs(tmp_path: Path) -> None:
    _seed_repo(tmp_path)
    scaf = Scaffolder(tmp_path)
    signals = scaf.discover()

    outputs = scaf.generate(signals, offline=True)

    assert set(outputs) == {"vision", "domain", "design", "patterns"}
    for key, body in outputs.items():
        assert body.startswith(f"# {key.capitalize()}\n")
        assert "auto-generated by `sendsprint init`" in body
        assert "### Detected" in body
        assert "_TODO:_" in body


