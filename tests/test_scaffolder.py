def test_write_creates_spec_files_and_skips_existing(tmp_path: Path) -> None:
    _seed_repo(tmp_path)
    scaf = Scaffolder(tmp_path)
    outputs = scaf.generate(scaf.discover(), offline=True)

    first = scaf.write(outputs)
    assert {p.name for p in first.created} == {"VISION.md", "DOMAIN.md", "DESIGN.md", "PATTERNS.md"}
    assert first.skipped == []
    for p in first.created:
        assert p.is_file()

    second = scaf.write(outputs)
    assert second.created == []
    assert {p.name for p in second.skipped} == {
        "VISION.md",
        "DOMAIN.md",
        "DESIGN.md",
        "PATTERNS.md",
    }


def test_write_force_overwrites(tmp_path: Path) -> None:
    _seed_repo(tmp_path)
    scaf = Scaffolder(tmp_path)
    outputs = scaf.generate(scaf.discover(), offline=True)
    scaf.write(outputs)

    overwritten = scaf.write(outputs, force=True)
    assert {p.name for p in overwritten.created} == {
        "VISION.md",
        "DOMAIN.md",
        "DESIGN.md",
        "PATTERNS.md",
    }
    assert overwritten.skipped == []


def test_write_targets_match_spec_files_map(tmp_path: Path) -> None:
    _seed_repo(tmp_path)
    scaf = Scaffolder(tmp_path)
    outputs = scaf.generate(scaf.discover(), offline=True)
    result = scaf.write(outputs)

    rel = {p.relative_to(tmp_path) for p in result.created}
    assert rel == set(SPEC_FILES.values())


