def test_discover_collects_signals(tmp_path: Path) -> None:
    _seed_repo(tmp_path)
    signals = Scaffolder(tmp_path).discover()

    assert signals.repo_path == tmp_path.resolve()
    assert signals.file_count >= 2
    assert "package.json" in signals.manifests
    assert "README.md" in signals.docs
    assert "react" in signals.primary_languages
    assert "init" in signals.git_log
    assert signals.fingerprint_json.startswith("{")


def test_discover_handles_missing_docs_and_manifests(tmp_path: Path) -> None:
    """Repo with only a tracked .gitkeep — no manifest, no docs."""
    tmp_path.mkdir(parents=True, exist_ok=True)
    (tmp_path / ".gitkeep").write_text("", encoding="utf-8")
    subprocess.run(["git", "init", "-q"], cwd=tmp_path, check=True)
    subprocess.run(["git", "add", "-A"], cwd=tmp_path, check=True)
    subprocess.run(
        ["git", "-c", "user.email=a@b.c", "-c", "user.name=Test", "commit", "-q", "-m", "init"],
        cwd=tmp_path,
        check=True,
    )

    signals = Scaffolder(tmp_path).discover()
    assert signals.manifests == {}
    assert signals.docs == {}
    assert signals.file_count >= 1


