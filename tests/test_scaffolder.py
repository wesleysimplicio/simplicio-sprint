def _seed_repo(repo: Path) -> None:
    """Init a git repo with a package.json + README so detect_tech finds 'react'."""
    repo.mkdir(parents=True, exist_ok=True)
    (repo / "package.json").write_text(
        json.dumps({"name": "smoke", "version": "1.0.0", "dependencies": {"react": "19"}}),
        encoding="utf-8",
    )
    (repo / "README.md").write_text("# Smoke\n\nDemo repo.\n", encoding="utf-8")
    subprocess.run(["git", "init", "-q"], cwd=repo, check=True)
    subprocess.run(["git", "add", "-A"], cwd=repo, check=True)
    subprocess.run(
        ["git", "-c", "user.email=a@b.c", "-c", "user.name=Test", "commit", "-q", "-m", "init"],
        cwd=repo,
        check=True,
    )


class _FakeLlm:
    """LLM stub — records prompts and returns a canned body keyed by section header."""

    def __init__(self) -> None:
        self.calls: list[tuple[str, str | None]] = []

    def complete(self, prompt: str, *, system: str | None = None, max_tokens: int = 2000) -> str:
        self.calls.append((prompt, system))
        for line in prompt.splitlines():
            if line.startswith("- ") and len(line) > 2:
                marker = line[2:].strip()
                return f"## {marker}\n\nGenerated body for {marker}.\n"
        return "## Stub\n\n_TODO:_\n"


