def test_run_offline_end_to_end(tmp_path: Path) -> None:
    _seed_repo(tmp_path)
    seen: list[str] = []
    result = Scaffolder(tmp_path).run(offline=True, on_step=seen.append)

    assert result.signals is not None
    assert len(result.created) == 4
    assert seen == ["vision", "domain", "design", "patterns"]
    for path in result.created:
        text = path.read_text()
        assert "auto-generated by `sendsprint init`" in text
        assert "_TODO:_" in text


